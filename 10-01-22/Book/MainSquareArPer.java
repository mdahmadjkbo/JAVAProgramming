import java.util.Scanner;
class MainSquareArPer{
    public static void main(String[]args){
	Scanner sc=new Scanner(System.in);
	    System.out.println();
	    System.out.print("Input: ");
	int s=sc.nextInt();
	int ar=s*s;
	int per=2*(s+s);
	    System.out.println();
	    System.out.println("Output: Area: "+ar);
	    System.out.println();
	    System.out.println("      Perimeter: "+per);}
}
 