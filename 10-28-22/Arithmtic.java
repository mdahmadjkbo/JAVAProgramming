class Arithmtic{
    public static void main(String[]ab){
	int a=Integer.parseInt(ab[0]);
	int b=Integer.parseInt(ab[1]);
	    System.out.println("Summation of a&b "+a+" & "+b+": "+(a+b));
	    System.out.println("Difference of a&b "+a+" & "+b+": "+(a-b));
	    System.out.println("Product of a&b "+a+" & "+b+": "+(a*b));
	    System.out.println("Factor(quotient) of a&b "+a+" & "+b+": "+(a/b));
	    System.out.println("Modulus(remainder) of a&b "+a+" & "+b+": "+(a%b));}
}